  if (starterData) {
    return {
      sprite: starterData.sprite,
      spriteSet: starterData.spriteSet,
      type: starterData.type,
      baseHP: starterData.maxHP,
      attacks: starterData.attacks,
      speed: starterData.speed || 50,
    }
  }

  const wildData = wildPokemon[pokemonName]
